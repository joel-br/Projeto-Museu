           connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_museu", "root", "");

           ps = connection.prepareStatement("SELECT 'usuario','senha'FROM 'tb_usuario' WHERE 'usuario'=? AND 'senha'=?");
